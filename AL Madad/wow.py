# Dictionary to store word-to-number mappings
word_to_number = {}

def assign_random_number(word):
    # Generate a random number between 1000 and 9999
    random_number = random.randint(1000, 9999)
    # Ensure the number is unique
    while random_number in word_to_number.values():
        random_number = random.randint(1000, 9999)
    # Assign the number to the word
    word_to_number[word] = random_number
    return random_number

def search_word(word):
    # Search for the number using the word
    if word in word_to_number:
        return word_to_number[word]
    else:
        return None  # Return None if the word is not found

# Assign random numbers to all Urdu words
for word in urdu_words:
    assigned_number = assign_random_number(word)
    print(f"Word: {word}, Assigned Number: {assigned_number}")

# Search for a word
search_word_input = input("Enter an Urdu word to search for its number: ")
result = search_word(search_word_input)

if result:
    print(f"The number for the word '{search_word_input}' is: {result}")
else:
    print(f"No number found for the word '{search_word_input}'.")



    
